int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	o;

	i = 0;
	o = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			o++;
		}
		i++;
	}
	if (o == i)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
