int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A' && str[i] <= 'Z'))
			;
		else if ((str[i] >= 'a' && str[i] <= 'z'))
			;
		else
			return (0);
		i++;
	}
	return (1);
}
