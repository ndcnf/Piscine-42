int	ft_str_is_numeric(char *str)
{
	int		i;
	int		o;

	i = 0;
	o = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= '0' && str[i] <= '9')
			o++;
		else
			return (0);
		i++;
	}
	if (o == i || o == 0)
		return (1);
	else
		return (0);
}
