int	ft_str_is_printable(char *str)
{
	int	i;
	int	o;

	i = 0;
	o = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 32 && str[i] <= 126)
			o++;
		i++;
	}
	if (i == o)
		return (1);
	else
		return (0);
}
